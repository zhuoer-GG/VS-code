package Indictment;
//在 Java 中，package Indictment; 的作用是声明当前文件属于 Indictment 这个包（package）。

//如果您复制了此代码，则需要将文件名另存为HelloWorld.java。
//这是因为类名和文件名在Java中应该相同。
class HelloWorld {//Java中，每个应用程序都以类定义开头
    //main方法是Java应用程序的入口点，并且在Java程序中是必需的
    public static void main(String[] args) {
        System.out.println("Hello, World!"); 
        //将引号内的字符串 Hello, World!，打印到标准输出
    }
}

//    每个有效的Java应用程序都必须具有一个类定义（与文件名匹配）。
//    main方法必须在类定义内。
//    编译器从主函数开始执行代码。